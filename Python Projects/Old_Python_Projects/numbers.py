#using maths functions to solve python problems
import math

pi=3.14
a=1
b=2
c=3

#different ways of implementing the maths module
#rounding off
print(round(pi))
#the value of pi
print(math.pi)
#ceiling value of pi
print(math.ceil(pi))
#absolute value of pi
print(abs(pi)) #tells you how far a number is from zero
#raising a number to its power
print(pow(2,3))
#finding the maximum number among the given numbers
print(max(a,b,c))
