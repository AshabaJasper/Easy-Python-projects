# False - explicitly assigning False to a boolean variable
bool_var1 = False

# False - None represents the absence of a value
bool_var2 = None

# False - 0 is considered as a False value in Python
bool_var3 = 0

# False - an empty string is considered as a False value
bool_var4 = ""

# False - an empty tuple is considered as a False value
bool_var5 = ()

# False - an empty list is considered as a False value
bool_var6 = []

# False - an empty dictionary is considered as a False value
bool_var7 = {}

# False - calling bool() without any argument returns False
bool_var8 = bool()

# Checking the boolean value of each variable
print(bool(bool_var1))  # False
print(bool(bool_var2))  # False
print(bool(bool_var3))  # False
print(bool(bool_var4))  # False
print(bool(bool_var5))  # False
print(bool(bool_var6))  # False
print(bool(bool_var7))  # False
print(bool(bool_var8))  # False
